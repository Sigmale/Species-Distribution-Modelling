# Convert coordinates to WKT format
def create_wkt(coordinates):
    # Format coordinates for WKT
    wkt_coordinates = ', '.join([f"{lon} {lat}" for lon, lat in coordinates])
    # Close the polygon by repeating the first coordinate
    wkt_coordinates += f", {coordinates[0][0]} {coordinates[0][1]}"
    # Create the WKT string
    wkt = f"POLYGON(({wkt_coordinates}))"
    return wkt

